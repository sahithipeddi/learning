package com.learning.DataStructures.BinaryTrees;

public class BinaryNodeAVL {

    public int height;

    public int value;

    public BinaryNodeAVL left;

    public BinaryNodeAVL right;

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public BinaryNodeAVL getLeft() {
        return left;
    }

    public void setLeft(BinaryNodeAVL left) {
        this.left = left;
    }

    public BinaryNodeAVL getRight() {
        return right;
    }

    public void setRight(BinaryNodeAVL right) {
        this.right = right;
    }
}
